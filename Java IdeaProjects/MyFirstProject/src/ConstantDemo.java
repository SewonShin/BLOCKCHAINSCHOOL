public class ConstantDemo {
    public static void main(String[] args) {
        double a = 2.2;
        float b = 2.2F;
        long c = 2147483648L;
        byte d = 100;
        short e = 200;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
